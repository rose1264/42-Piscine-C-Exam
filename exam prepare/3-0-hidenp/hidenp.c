#include <unistd.h>

void	hidenp(char *s1, char *s2)
{
	int i;

	i = 0;
	while (s2[i])
	{
		if (*s1 == s2[i])
			s1++;
		i++;
	}
	(*s1 == '\0') ? write(1, "1", 1) : write(1, "0", 1);
}

int		main(int argc, char **argv)
{
	if (argc == 3)
		hidenp(argv[1], argv[2]);
	write(1, "\n", 1);
	return (0);
}
